# 比较运算
num1 = 10
num2 = 20
print(f"10 == 20的结果是：{num1 == num2}")
print(f"10 != 20的结果是：{num1 != num2}")
print(f"10 > 20的结果是;{num1 > num2}")
print(f"10 < 20的结果是：{num1 < num2}")
print(f"10 >= 20的结果是：{num1 >= num2}")
print(f"10 <= 20的结果是：{num1 <= num2}")
