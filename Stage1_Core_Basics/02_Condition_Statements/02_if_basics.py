# if语句的基本格式
age = 30
if age >= 18:
    print("我已经成年了")
    print("即将步入大学生活")

print("时间过得真快呀")

if age >= 80:
    print("我已经老年了")
    print("即将步入享福生活")

print("时间过得真快呀")




