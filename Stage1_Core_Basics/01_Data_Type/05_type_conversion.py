# 转字符串
str_num = str(1234)
print(type(str_num),str_num)

str_float = str(12.34)
print(type(str_float),str_float)



# 转整数
num_float = int(12.34)
print(type(num_float),num_float)

num_intstr = int("1234")
print(type(num_intstr),num_intstr)

